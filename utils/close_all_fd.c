#include "../inc/pipex.h"

void	close_all_fd(t_ptrs *ptrs)
{
	int	x;

	x = 0;
	while (x < ptrs->i)
	{
		if (ptrs->fd[x][0] > 2)
			close(ptrs->fd[x][0]);
		if (ptrs->fd[x][1] > 2)
			close(ptrs->fd[x][1]);
		x++;
	}
}
